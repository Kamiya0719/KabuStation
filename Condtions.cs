						IsAndCheck ? i : -1, !IsAndCheck ? i : -1, IsAndCheck ? kouhoList[i] : -1, !IsAndCheck ? kouhoList[i] : -1,
						j, info, confirmBeny
					);
					condResList.Add(condRes);
					condResAll.Add(condRes);
				}

