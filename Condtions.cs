		private static void CheckCond51AllBase(int andSkip, int orSkip)
		{
			(int[] confirmAnds, int[] confirmOrs) = GetConfirms();

			bool isOrOkForce = IsAndCheck && confirmOrs.Length == 0 && KouhoOrs.Length == 0; // orチェックを強制でOKにしておく

